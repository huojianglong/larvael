Route::group(['prefix'=>'Home'],function(){
    //前台登录
    Route::group(['prefix'=>'Index'],function(){
        Route::get("/login",['uses'=>'Home\IndexController@login']);
    });

    //商品组
    Route::group(['prefix'=>'Goods'],function(){
        Route::get('/goodsInfo/{id?}',['uses'=>'Home\GoodsController@goodsInfo']);

        //收藏
        Route::get('/collection',['uses'=>'Home\GoodsController@collection']);

        //足迹
        Route::get('/goodsfoot',['uses'=>'Home\GoodsController@goodsfoot']);
        Route::post('/footAction',['uses'=>'Home\GoodsController@footAction']);
    });

    //前台购物车
    Route::group(['prefix'=>'Cart'],function(){
        Route::get('/index',['uses'=>'Cart\CartController@index']);

        //购物车商品信息处理
        Route::post('/indexAction',['uses'=>'Cart\CartController@indexAction']);

        //商品详情添加购物车处理
        Route::post('/infoAction',['uses'=>'Cart\CartController@infoAction']);

        //购物车结算生成订单
        Route::post('/cartAction',['uses'=>'Cart\CartController@cartAction']);
        Route::get('/pay/',['uses'=>'Cart\CartController@pay']);

    });





});
